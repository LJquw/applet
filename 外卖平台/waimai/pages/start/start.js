  /**
   * 生命周期函数--监听页面加载
   */
  onLoad: function (options) {
    //获取本地缓存的userInfo信息，如果能拿到,那么直接到index页面
    wx.getStorage({
      key: 'userInfo',
      success (res) {
        //直接跳转页面
        //跳转页面---首页   tabbar的页面
        wx.switchTab({
          url: '/pages/index/index'
        })
      }
    })

  },
  getUserProfile(e) {
    // 推荐使用wx.getUserProfile获取用户信息，开发者每次通过该接口获取用户个人信息均需用户确认
    // 开发者妥善保管用户快速填写的头像昵称，避免重复弹窗
    wx.getUserProfile({
      desc: '用于完善会员资料', // 声明获取用户个人信息后的用途，后续会展示在弹窗中，请谨慎填写
      success: (res) => {
        console.log("获取用户信息",res.userInfo);
        //把数据放入本地缓存
        wx.setStorage({
          key:"userInfo",
          data:res.userInfo
        })
        //跳转页面---首页   tabbar的页面
        wx.switchTab({
          url: '/pages/index/index'
        })

      }
    })
  },
